// Source language. Selects both the `-lang` flag and the source file extension
// (MWCC picks C vs C++ partly by extension), so it is NOT expressible via
// extra_flags alone.
#[derive(Clone, Copy, PartialEq, Eq)]
pub enum Language {
    C,
    Cpp,
}

impl Language {
    pub fn parse(s: Option<&str>) -> Self {
        match s.map(str::trim) {
            Some("cpp") | Some("c++") | Some("cxx") => Language::Cpp,
            _ => Language::C,
        }
    }
    fn lang_flag(self) -> &'static str {
        match self {
            Language::C => "-lang=c",
            Language::Cpp => "-lang=c++",
        }
    }
    fn ext(self) -> &'static str {
        match self {
            Language::C => "c",
            Language::Cpp => "cpp",
        }
    }
}

// Optimization presets the frontend may request. A validated allow-list (rather
// than free-form `-O` strings) keeps the surface safe — `validate_opt` falls back
// to the default for anything unknown. `O4,p` reproduces today's behaviour.
pub const ALLOWED_OPT: &[&str] = &["O0", "O1", "O2,p", "O2,s", "O3,p", "O3,s", "O4,p", "O4,s"];
pub const DEFAULT_OPT: &str = "O4,p";

pub fn validate_opt(s: Option<&str>) -> String {
    match s.map(str::trim) {
        Some(v) if ALLOWED_OPT.contains(&v) => v.to_string(),
        _ => DEFAULT_OPT.to_string(),
    }
}

// One MWCC build. The registry (`COMPILERS`) is generated by build.rs from
// compilers.list so the binary, the fetch script, and the template generator all
// share one manifest. The same binary ships to every per-version Lambda; each
// Lambda's `MWCC_VERSION` env var selects its entry here.
pub struct Compiler {
    /// decomp.me id, e.g. "mwcc_247_92"; matched against the MWCC_VERSION env.
    pub id: &'static str,
    /// URL version segment, e.g. "247_92" -> /compile/mwcc/247_92/{action}.
    pub token: &'static str,
    /// Path to mwcceppc.exe under vendor/, e.g. "compiler/GC/2.0/mwcceppc.exe".
    pub rel_path: &'static str,
    /// Human label for /health.
    pub label: &'static str,
}

include!(concat!(env!("OUT_DIR"), "/compilers_generated.rs"));

// GC MW 2.0 — the historical default. Owns the legacy flat routes, and the value
// every code path falls back to when MWCC_VERSION is unset (local dev, tests).
pub const DEFAULT_VERSION_ID: &str = "mwcc_247_92";

/// The compiler this Lambda is configured to run, from `MWCC_VERSION` (falling
/// back to the default). Looked up once per call — cheap over a linear scan of a
/// few dozen entries, and avoids any global init.
pub fn active_compiler() -> &'static Compiler {
    compiler_for(&std::env::var("MWCC_VERSION").unwrap_or_default())
}

/// Resolve a compiler by decomp.me id, falling back to the default for an
/// unset/unknown id. Split out from `active_compiler` so it's testable without
/// racing on the process-global `MWCC_VERSION` env var.
fn compiler_for(id: &str) -> &'static Compiler {
    COMPILERS
        .iter()
        .find(|c| c.id == id)
        .or_else(|| COMPILERS.iter().find(|c| c.id == DEFAULT_VERSION_ID))
        .expect("default compiler present in registry")
}

// Legacy body `compiler` field — superseded by the URL, ignored on versioned
// routes. Kept lenient for the flat routes the current frontend still calls
// (which send nothing, "GC/2.0", or "mwcc").
pub fn validate_compiler(s: Option<&str>) -> Result<(), String> {
    match s.map(str::trim) {
        None | Some("") | Some("mwcc") | Some("GC/2.0") => Ok(()),
        Some(other) => Err(format!(
            "Unsupported compiler '{other}'. This service compiles with MWCC; pick a \
             version via /compile/mwcc/<version>/<action>."
        )),
    }
}

/// True if `token` names a known MWCC build. Each version has its own Lambda +
/// literal route, so in practice the route already guarantees a valid token; this
/// guards direct/local invocation and yields a helpful error for typos.
pub fn validate_version(v: &str) -> Result<(), String> {
    let v = v.trim();
    if COMPILERS.iter().any(|c| c.token == v) {
        Ok(())
    } else {
        Err(format!("Unsupported mwcc version '{v}'."))
    }
}

// Assemble the compiler flag list. The opt token and `-lang` token occupy the
// SAME positions the old hardcoded set used, so the default C command line is
// byte-identical to before (no codegen drift). Peephole/scheduling are disabled
// by appending `-opt nopeephole` / `-schedule off` (both verified to match the
// in-source `#pragma peephole/scheduling off` the curriculum uses).
fn base_flags(language: Language, opt: &str, peephole: bool, schedule: bool) -> Vec<String> {
    let mut f: Vec<String> = [
        "-nodefaults", "-proc", "gekko", "-align", "powerpc", "-enum", "int", "-fp", "hardware",
        "-Cpp_exceptions", "off",
    ]
    .iter()
    .map(|s| s.to_string())
    .collect();
    f.push(format!("-{opt}"));
    for s in ["-inline", "auto", "-maxerrors", "1", "-nosyspath", "-RTTI", "off", "-fp_contract", "on"] {
        f.push(s.to_string());
    }
    f.push(language.lang_flag().to_string());
    if !peephole {
        f.push("-opt".into());
        f.push("nopeephole".into());
    }
    if !schedule {
        f.push("-schedule".into());
        f.push("off".into());
    }
    f
}

