// Port of the original Node compile/asm modules. Drives the vendored MWCC GC/2.0
// compiler (via wibo) and gekko objdump, then extracts each function's symbol +
// instruction count. The browser does the real instruction diff with
// objdiff-wasm against the raw object file, so we don't normalize asm here — we
// only need the symbol list, per-symbol instruction counts, and the object file.

